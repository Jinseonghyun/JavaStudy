package lang.string;

public class CharArrayMain {

    public static void main(String[] args) {
        char a ='가';
        System.out.println("a = " + a);

        char[] charArr = new char[]{'h', 'e', 'l', 'l', 'o'};  // char 을 문장으로
        System.out.println(charArr);

        String str = "hello";
        System.out.println("str = " + str);  // 문자열
    }
}
